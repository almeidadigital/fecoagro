-- =============================================
-- plano_contas (inbound FKs from critica.plano_conta_id and razao.plano_conta_id)
-- =============================================
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'plano_contas'
    AND column_name = 'id' AND data_type = 'uuid'
  ) THEN
    ALTER TABLE public.plano_contas ADD COLUMN new_id BIGINT GENERATED BY DEFAULT AS IDENTITY;
    CREATE TEMP TABLE map_pc AS SELECT id as old_id, new_id as new_id FROM public.plano_contas;

    -- Update critica.plano_conta_id
    ALTER TABLE public.critica ADD COLUMN new_plano_conta_id BIGINT;
    UPDATE public.critica SET new_plano_conta_id = m.new_id FROM map_pc m WHERE critica.plano_conta_id = m.old_id;
    ALTER TABLE public.critica DROP CONSTRAINT IF EXISTS transactions_plano_conta_id_fkey;
    ALTER TABLE public.critica DROP CONSTRAINT IF EXISTS critica_plano_conta_id_fkey;
    ALTER TABLE public.critica DROP COLUMN plano_conta_id;
    ALTER TABLE public.critica RENAME COLUMN new_plano_conta_id TO plano_conta_id;

    -- Update razao.plano_conta_id
    ALTER TABLE public.razao ADD COLUMN new_plano_conta_id BIGINT;
    UPDATE public.razao SET new_plano_conta_id = m.new_id FROM map_pc m WHERE razao.plano_conta_id = m.old_id;
    ALTER TABLE public.razao DROP CONSTRAINT IF EXISTS razao_plano_conta_id_fkey;
    ALTER TABLE public.razao DROP COLUMN plano_conta_id;
    ALTER TABLE public.razao RENAME COLUMN new_plano_conta_id TO plano_conta_id;

    -- Update plano_contas table
    ALTER TABLE public.plano_contas DROP CONSTRAINT plano_contas_pkey;
    ALTER TABLE public.plano_contas DROP COLUMN id;
    ALTER TABLE public.plano_contas RENAME COLUMN new_id TO id;
    ALTER TABLE public.plano_contas ADD PRIMARY KEY (id);

    -- Recreate FKs
    ALTER TABLE public.critica ADD CONSTRAINT critica_plano_conta_id_fkey
      FOREIGN KEY (plano_conta_id) REFERENCES public.plano_contas(id) ON DELETE SET NULL;
    ALTER TABLE public.razao ADD CONSTRAINT razao_plano_conta_id_fkey
      FOREIGN KEY (plano_conta_id) REFERENCES public.plano_contas(id) ON DELETE SET NULL;
    DROP TABLE map_pc;
  END IF;
END $$;
