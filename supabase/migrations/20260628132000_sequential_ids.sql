-- Convert UUID primary keys to sequential BIGINT for bancos, atividades, centro_custos, plano_contas

-- =============================================
-- bancos (no inbound FKs)
-- =============================================
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'bancos'
    AND column_name = 'id' AND data_type = 'uuid'
  ) THEN
    ALTER TABLE public.bancos ADD COLUMN new_id BIGINT GENERATED BY DEFAULT AS IDENTITY;
    ALTER TABLE public.bancos DROP CONSTRAINT bancos_pkey;
    ALTER TABLE public.bancos DROP COLUMN id;
    ALTER TABLE public.bancos RENAME COLUMN new_id TO id;
    ALTER TABLE public.bancos ADD PRIMARY KEY (id);
  END IF;
END $$;

-- =============================================
-- atividades (inbound FK from critica.atividade_id)
-- =============================================
DO $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_schema = 'public' AND table_name = 'atividades'
    AND column_name = 'id' AND data_type = 'uuid'
  ) THEN
    ALTER TABLE public.atividades ADD COLUMN new_id BIGINT GENERATED BY DEFAULT AS IDENTITY;
    CREATE TEMP TABLE map_ativ AS SELECT id as old_id, new_id as new_id FROM public.atividades;

    ALTER TABLE public.critica ADD COLUMN new_atividade_id BIGINT;
    UPDATE public.critica SET new_atividade_id = m.new_id FROM map_ativ m WHERE critica.atividade_id = m.old_id;
    ALTER TABLE public.critica DROP CONSTRAINT IF EXISTS transactions_atividade_id_fkey;
    ALTER TABLE public.critica DROP CONSTRAINT IF EXISTS critica_atividade_id_fkey;
    ALTER TABLE public.critica DROP COLUMN atividade_id;
    ALTER TABLE public.critica RENAME COLUMN new_atividade_id TO atividade_id;

    ALTER TABLE public.atividades DROP CONSTRAINT atividades_pkey;
    ALTER TABLE public.atividades DROP COLUMN id;
    ALTER TABLE public.atividades RENAME COLUMN new_id TO id;
    ALTER TABLE public.atividades ADD PRIMARY KEY (id);

    ALTER TABLE public.critica ADD CONSTRAINT critica_atividade_id_fkey
      FOREIGN KEY (atividade_id) REFERENCES public.atividades(id) ON DELETE SET NULL;
    DROP TABLE map_ativ;
  END IF;
END $$;

